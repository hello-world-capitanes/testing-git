function suma(a, b) {
    return a + b;
}

function multiplica(a, b) {
    return a * b;
}

function resta(a, b) {
    // Comentario Manu
    return a - b;
}

function divide(a, b) {
    return a / b;
}

function eleva(a, b) {
   return Math.pow(a,b);
}

function raiz(a) {
   return Math.sqrt(a)
}

function maximo(a, b){
   return Math.max(a, b);
}

function minimo(a, b){
    return Math.min(a, b);
}

function redondea(a) {
    return Math.round(a);
}

